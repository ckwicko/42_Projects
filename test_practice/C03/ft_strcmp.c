#include <stdio.h>

int	ft_strcmp(char *s1, char *s2)
{
	int	i;

	i = 0;
	while (s1[i] && s2[i])
	{
		if (s1[i] != s2[i])
			return (s1[i] - s2[i]);
		i++;
	}
	return (0);
}

int	main(void)
{
	char	str1[50] = "helLo";
	char	str2[50] = "heLLo";

	printf("%d\n", ft_strcmp(str1, str2));
	return (0);
}
